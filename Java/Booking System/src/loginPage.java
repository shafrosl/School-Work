    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String itemSelected = jComboBox1.getSelectedItem() + "";
        
        if (itemSelected.equals("Staff")) {
            passStud.setVisible(false);
            userStud.setVisible(false);
            userStaff.setVisible(true);
            passStaff.setVisible(true);
            login.setVisible(true);
            check = 1;
        }
        
        else if (itemSelected.equals("Student")) {
            userStaff.setVisible(false);
            passStaff.setVisible(false);
            passStud.setVisible(true);
            userStud.setVisible(true);
            login.setVisible(true);
            check = 2;
        }
        
        else {
            userStaff.setVisible(false);
            passStaff.setVisible(false);
            passStud.setVisible(false);
            userStud.setVisible(false);
            login.setVisible(false);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

