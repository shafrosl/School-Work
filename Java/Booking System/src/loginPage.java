class studentFile extends loginPage{
    String ps,un;
    String [] line;
    String studentfile = directory.student; 
    
    public int studentFile1(String u, String p)throws IOException{
        Scanner input = new Scanner (new File(studentfile));
        String str, id;
        
        while (input.hasNextLine()) {
            str = input.nextLine();
            line = str.split(",");
            un = line[0];
            ps = line[1];
            id = line[2];
            userID userID = new userID();
            userID.setID(id);
            
            if ((ps.equals(p)) && (un.equals(u))){
                return 1;
            }
        }
           
        return 2;
    }
}

